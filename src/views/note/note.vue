메인 페이지에 버튼 1~4번까지 있음
자기소개 버튼 누르면 자기소개 페이지 이동
가이드 버튼 누르면 html, vue 버튼 보임
  - 각각 버튼 누르면 페이지 보임
정리노트 버튼 누르면 html, css, script, vue 버튼 보임
  - 각각 버튼 누르면 페이지 보임
프로젝트 경력 버튼 누르면 프로젝트 경력 페이지 이동

상세하게
자기소개
  - 사진(이건 있어도되고 없어도 되고)
  - 이력서 형식

가이드(사실상 가이드가 메인)
  - vue 페이지 가이드 정리
  - 각각의 가이드 누르면 상세 가이드 나옴
  - 상세 가이드 아코디언 누르면 코드 나옴

정리노트
  - 텍스트 위주의 정리

프로젝트 경력
  - 리스트 형식으로 뿌려서 프로젝트 링크 걸기
